export type MainDirectionsState = {
  items: readonly MainDirectionItem[];
};

export const useMainDirectionsStore = create<MainDirectionsState>(() => ({
  items: INITIAL_ITEMS,
}));

export function findDirectionBySlug(
  items: readonly MainDirectionItem[],
  slug: string | undefined
): MainDirectionItem | undefined {
  if (!slug) return undefined;
  return items.find((item) => item.slug === slug);
}
